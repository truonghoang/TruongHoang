public void setModel(Customer ee){
    txtName.setText(ee.getTenKH());
    
    rdnam.setSelected(ee.isGioitinh());
    rdnu.setSelected(!ee.isGioitinh());
    txtngaysinh.setText(date_format.format(ee.getNgaysinh()));
    txtSdt.setText(ee.getSdt());
    
}
    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
      
        if (txtSdt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập SĐT!!");
        } else {
            Customer ee = dao.findBysdt(txtSdt.getText());
            if (ee != null) {
                setModel(ee);
                st.setText("Tìm Thấy Khách Hàng Khả Dụng !!");
            } else {
                st.setText("Không tìm thấy khách hàng : " + txtSdt.getText());
                st.setForeground(Color.red);
            }
        }
    }//GEN-LAST:event_btnfindActionPerformed

