   public void filldata(){
       tbmodel =(DefaultTableModel) tbcus.getModel();
       tbmodel.setRowCount(0);
       for(Customer e: dao.getAllkhachhang()){
           Object[] rowdata = new Object[4];
           rowdata[0]= e.getTenKH();
           String gt ="Nữ";
           if(e.isGioitinh()){
               gt="Nam";
           }
           rowdata[1]=gt;
           rowdata[2]= date_format.format(e.getNgaysinh());
           rowdata[3]=e.getSdt();
           tbmodel.addRow(rowdata);
            lsCu.add(e);
           file.ghi1(lsCu);
       }
   }
  
      public  Customer getModel() throws ParseException {
        Customer ee = new Customer();
        ee.setTenKH(txtName.getText());

        boolean gt = false;
        if (rdnam.isSelected()) {
            gt = true;
        }
        ee.setGioitinh(gt);
        ee.setNgaysinh(date_format.parse(txtngaysinh.getText()));
        ee.setSdt(txtSdt.getText());
        return ee;
    }
    public boolean checkdata() {
        if (txtName.getText().isEmpty() || txtSdt.getText().isEmpty() ) {
            return false;
        }else if(txtName.getText().matches("[^a-zA-Z]+")){
            JOptionPane.showMessageDialog(this, "Tên Không chứa ký tự đặc biệt");
            return false;
        }else if(txtSdt.getText().matches("[^0-9]+")){
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ");
            return false;
        }
        return true;
    }
    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if(checkdata()){
            try {
                Customer e = getModel();
                if (dao.add(e) > 0) {
                    st.setText("Lưu Thành Công!!");
                    st.setForeground(Color.blue);
                    not1.setText("V");
                    not1.setForeground(Color.GREEN);
                    not2.setText("V");
                    not2.setForeground(Color.GREEN);
                    not3.setText("V");
                    not3.setForeground(Color.GREEN);
                    slk.setText("Số Lượng Khách Hàng: "+dao.count1());
                    filldata();
                  
                } else {
                    
                    st.setText("Lưu Không Thành Công!!");
                    st.setForeground(Color.red);
                }
                
             } catch (ParseException ex) {
                System.out.println(""+ex.toString());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Thông Tin Chưa Đầy Đủ!!");
            not1.setText("*");
                    not1.setForeground(Color.red);
                    not2.setText("*");
                    not2.setForeground(Color.red);
                    not3.setText("*");
                    not3.setForeground(Color.red);
        }
            
    }//GEN-LAST:event_btnsaveActionPerformed
